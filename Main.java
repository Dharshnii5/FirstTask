public class Main {
    public static void main(String[]args) {
        for (int i = 1; i <= 50; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        int j = 1;
        while (j <= 50) {
            System.out.print(j + " ");
            j++;
        }


    }
}
